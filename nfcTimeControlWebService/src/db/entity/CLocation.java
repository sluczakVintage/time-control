package db.entity;
import java.sql.Timestamp;
public class CLocation {
	private long id;
	private String street;
	private String city;
	private String postCode;
	private String details;
	private String tagID;
	private String name;
	private Timestamp creationDate;
	private String status;
	private CUsers creator;
	private String serialNumber;
	private CCompany company;
	

	
	public CLocation() {}
	
	public CLocation(String street, String city, String postCode,
			String details, String tagID, String name, Timestamp creationDate,
			String status, CUsers creator, CCompany company, String serialNumber) {
		super();
		this.street = street;
		this.city = city;
		this.postCode = postCode;
		this.details = details;
		this.tagID = tagID;
		this.name = name;
		this.creationDate = creationDate;
		this.status = status;
		this.creator = creator;
		this.company = company;
		this.serialNumber = serialNumber;
	}
	
	public void setId(long id) {
		this.id = id;
	}
	
	public long getId() {
		return id;
	}
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		this.street = street;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getPostCode() {
		return postCode;
	}
	public void setPostCode(String postCode) {
		this.postCode = postCode;
	}
	public String getDetails() {
		return details;
	}
	public void setDetails(String details) {
		this.details = details;
	}
	public String getTagID() {
		return tagID;
	}
	public void setTagID(String tagID) {
		this.tagID = tagID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Timestamp getCreationDate() {
		return creationDate;
	}
	public void setCreationDate(Timestamp creationDate) {
		this.creationDate = creationDate;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}

	public CUsers getCreator() {
		return creator;
	}

	public void setCreator(CUsers creator) {
		this.creator = creator;
	}

	public CCompany getCompany() {
		return company;
	}

	public void setCompany(CCompany company) {
		this.company = company;
	}

	
	public String getSerialNumber() {
		return serialNumber;
	}
	public void setSerialNumber(String sn) {
		this.serialNumber = sn;
	}
	
}
