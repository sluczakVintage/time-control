public class CUserPrivileges {
	private long id;
	private CUsers user;
	private String role;
	
	public CUserPrivileges() {}
	
	public CUserPrivileges(CUsers user, String role) {
		super();
		this.user = user;
		this.role = role;
	}
	
	public void setId(long id) {
		this.id = id;
	}
	
	public long getId() {
		return id;
	}
	public CUsers getUser() {
		return user;
	}
	public void setUser(CUsers user) {
		this.user = user;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	
	
}
