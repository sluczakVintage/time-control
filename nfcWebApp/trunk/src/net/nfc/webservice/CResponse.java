package net.nfc.webservice;

import javax.xml.bind.annotation.XmlRootElement;
/**
 * 
 * @author Maciek
 *
 */
@XmlRootElement
public class CResponse {

	public String status;
	public String message;
	public String token;
	
	public CResponse() {} 
	
	public CResponse(String status, String msg, String token) {
		this.status = status;
		this.token = token;
		this.message = msg;
	}
	
}
